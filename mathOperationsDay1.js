//exportable function to add two numbers
export function add(a,b){
    return a+b;
}
//exportable function to substarct two numbers
export function substract(a,b){
    return a-b;
}
//exportable function to multiply two numbers
export function multiply(a,b){
    return a*b;
}
//exportable function to divide two numbers
export function divide(a,b){
    return a/b;
}

// exportable default function to find square
export default function square(a){
    return a*a;

}